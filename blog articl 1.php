            <h1>The Advantages of Cotton Clothing</h1>
            <p>The world uses cotton more than any other natural fiber and it is primarily grown and used to make cloth. Other parts of the cotton plant are put to good use and are used in the production of foods, plastics and in paper products, according to the National Cotton Council of America. Because cotton is a natural product and because of the way it is designed and manufactured into clothing, it has many advantages, such as its ability to control moisture, insulate, provide comfort and it is also hypoallergenic, weatherproof and is a durable fabric</p>
            <h3>Moisture Control</h3>
            <p> Cotton fabric is breathable and transmits moisture away from the body and is absorbent and removes liquid from the skin, like a towel, according to Cotton Incorporated. Cotton allows you to remain comfortable as you exercise, keeping moisture from building up between your skin and clothing. The International Forum for Cotton Promotion states that cotton can take up to one-fifth of its weight in water before feeling damp.</p>
            <h3>Insulation</h3>
             <p>Cotton clothing protects against from heat in the summer and cold in the winter by providing thermal insulation as the cotton fabric traps air between the fabric fibers. The cotton fibers in clothing hold the fabric away from the skin, further allowing for more air to be trapped between the skin and fabric which helps with insulation and comfort.</p>
            <h3>Hypoallergenic</h3>
            <p>Cotton fabric rarely causes allergic reactions and wearing cotton is often recommended for those with skin allergies, notes Cotton Incorporated. Because cotton is hypoallergenic and does not irritate skin, it is used in medical products like bandages and gauze, and is the fabric of choice when it comes to baby clothing.</p>
            <h3>Weatherproof</h3>
             <p>Cotton fabrics can easily be manufactured into weather-resistant garments through construction and finishing of the fabric. For example, cotton can be made into a tight, dense fabric with a weather-repellent finish to make weather resistant clothing, yet the cotton fabric retains its comfort and breathability.</p>
